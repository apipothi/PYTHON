# -Ve   =  -5,  -4, -3, -2, -1
# List  = [ 10, 20, 30, 40, 50 ]
# +Ve   =   0,   1,  2,  3,  4

list  = [ 10, 20, 30, 40, 50 ]
print(list) # [10, 20, 30, 40, 50]
print("------- Slice() Using -Ve Index -------")
print("[0:5]",list[0:5]) # [10, 20, 30, 40, 50]
print("[0:5:1]",list[0:5:1]) # [10, 20, 30, 40, 50]
print("[0:5:2]",list[0:5:2]) # [10, 30, 50]
print("[0:5:3]",list[0:5:3]) # [10, 40]














